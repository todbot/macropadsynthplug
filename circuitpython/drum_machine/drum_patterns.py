def copy_sequence_old(p):
    s = []
    for i in range(len(p)):
        s.append( p[i].copy() )
    return s

def make_sequence(p):
    s = []
    for i in range(p['len']):
        s.append( p['base'][ i % len(p['base']) ].copy() )
    return s

